            <h2>Why learn Toki Pona?</h2>
            <p>Toki pona is fundimentally built around the idea of simplicity. It has a vocabulary of less than two hundred words, and a perfectly regular grammar. This simplicity forces speakers to focus on the core point of what they're trying to say, and the altered structure from English can highlight some of the ways language shapes our thought. Of course this is a benifit that one can get from learning almost any second language, so why Toki Pona specifically? Well it's simplicity has a secondary benifit, it makes it incredibly easy to learn. While it might take a German student a year to reach a conversational skill level, a toki pona speaker can easily become conversational in a month, and fluent in a year. Are you ready to get started? If so go ahead and click on the link for lesson one.</p>
